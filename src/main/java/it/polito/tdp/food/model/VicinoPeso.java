package it.polito.tdp.food.model;

public class VicinoPeso {
	
	String vicino;
	int peso;
	
	public VicinoPeso(String vicino, int peso) {
		super();
		this.vicino = vicino;
		this.peso = peso;
	}

	public String getVicino() {
		return vicino;
	}

	public void setVicino(String vicino) {
		this.vicino = vicino;
	}

	public int getPeso() {
		return peso;
	}

	public void setPeso(int peso) {
		this.peso = peso;
	}
	
	
	

}
